; ─── INIT ───
LOAD_R0 100     ; 'D' (Move Right initially)
STORE_R0 255    ; Set sysLastKey
STORE_R0 250    ; Direction

LOAD_R0 100
STORE_R0 252    ; ApplePos = 100
LOAD_R1 252
LOAD_R0 128
STOREIND_R0_R1  ; Draw initial Apple

LOAD_R0 117
STORE_R0 253    ; HeadPos = 117
LOAD_R0 0
STORE_R0 248    ; TailIdx = 0
LOAD_R0 1
STORE_R0 249    ; HeadIdx = 1
LOAD_R0 116
STORE_R0 224    ; Queue[0] = 116

LOAD_R0 255
LOAD_R1 224
STOREIND_R0_R1  ; Draw initial Tail

; ─── MAIN LOOP ───
MAIN_LOOP:
LOADM_R0 255
STORE_R0 250    ; Direction = LastKey

LOADM_R0 250
STORE_R0 240
LOAD_R1 119     ; 'w'
SUB_R0_R1
JZ MOVE_UP

LOADM_R0 240
LOAD_R1 100     ; 'd'
SUB_R0_R1
JZ MOVE_RIGHT

LOADM_R0 240
LOAD_R1 115     ; 's'
SUB_R0_R1
JZ MOVE_DOWN

LOADM_R0 240
LOAD_R1 97      ; 'a'
SUB_R0_R1
JZ MOVE_LEFT

JMP UPDATE_QUEUE

MOVE_UP:
LOADM_R0 253
SUB_R0_IMM 16
STORE_R0 253
JMP CHECK_APPLE

MOVE_RIGHT:
LOADM_R0 253
ADD_R0_IMM 1
STORE_R0 253
JMP CHECK_APPLE

MOVE_DOWN:
LOADM_R0 253
ADD_R0_IMM 16
STORE_R0 253
JMP CHECK_APPLE

MOVE_LEFT:
LOADM_R0 253
SUB_R0_IMM 1
STORE_R0 253
JMP CHECK_APPLE

CHECK_APPLE:
LOADM_R0 253
LOADM_R1 252
SUB_R0_R1
JNZ NOT_APPLE

; Ate Apple
LOADM_R0 254
STORE_R0 252
LOAD_R1 252
LOAD_R0 128
STOREIND_R0_R1
JMP DRAW_HEAD

NOT_APPLE:
; Delete Tail
LOADM_R0 248
ADD_R0_IMM 224
STORE_R0 240
LOAD_R1 240
LOADIND_R0_R1
STORE_R0 241
LOAD_R1 241
LOAD_R0 0
STOREIND_R0_R1

LOADM_R0 248
ADD_R0_IMM 1
STORE_R0 248
LOAD_R1 16
SUB_R0_R1
JNZ DRAW_HEAD
LOAD_R0 0
STORE_R0 248

DRAW_HEAD:
; Self Collision
LOADM_R0 253
STORE_R0 240
LOAD_R1 240
LOADIND_R0_R1
LOAD_R1 255
SUB_R0_R1
JZ GAME_OVER

; Paint Head
LOAD_R0 255
LOAD_R1 240
STOREIND_R0_R1

UPDATE_QUEUE:
LOADM_R0 249
ADD_R0_IMM 224
STORE_R0 240
LOADM_R0 253
LOAD_R1 240
STOREIND_R0_R1

LOADM_R0 249
ADD_R0_IMM 1
STORE_R0 249
LOAD_R1 16
SUB_R0_R1
JNZ MAIN_LOOP
LOAD_R0 0
STORE_R0 249
JMP MAIN_LOOP

GAME_OVER:
HALT
